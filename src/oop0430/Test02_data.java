package oop0430;

public class Test02_data {

	public static void main(String[] args) {
		//프로그램의 전체적인 흐름 : 입력 -> 가공 -> 출력
		
		//콘솔창 출력
		//단축키 syso입력후 Ctrl+space
		//System.out.println(123);
		//System.out.println(456);
		
		
		//값(data, value)
		//1. 숫자형
		System.out.println(3);    //양의 정수
		System.out.println(-5);   //음의 정수
		System.out.println(2.4);  //실수
		System.out.println(-5.6); //실수
		System.out.println(7.0);  //실수
		System.out.println(8.);   //실수		
		
		//2. 문자형(String)
		//-> " 기호로 감싼다
		System.out.println("KOREA"); 
		System.out.println("대한민국");
		System.out.println("123");    //문자형

		//3. 논리형(boolean)
		//-> 맞다, 틀리다
		System.out.println(true);  //맞다(참)
		System.out.println(false); //틀리다(거짓)
		
		System.out.println("true"); //문자열형
		

	}//main() end
}//class end
